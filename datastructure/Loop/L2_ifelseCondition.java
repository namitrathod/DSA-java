package Loop;
// package datastructure;

public class L2_ifelseCondition {
        public static void main(String[] args) {

        // IF
        
            int a =10;
            if (a ==10){
                System.out.println("hello");
            }

            //While

            int count =1;
            while(count !=5) {
                System.out.println(count);
                count++;
            }


            // FOR

            for(int counts = 1; counts!=5; counts++ ){
                System.out.println(counts);
            }
        }
}
